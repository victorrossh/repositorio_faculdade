produto = int(input("Digite números de 1 até 10: "))

if produto == 1:
    print("Alimento não perecível")

elif produto == 2 or produto ==  3  or produto ==  4:
    print("Alimento perecível")

elif produto == 5 or produto == 6:
    print("Vestuário")      

elif produto == 7: 
    print("Higiene Pessoal")

elif produto == 8 or produto == 9 or produto == 10:
    print("Utensílios domésticos")

else:
    print("Código ")